import Foundation

/// Project font size
enum FontSize: CGFloat {
    /// Headline value is 24.0
    case headline = 24.0
    /// SubHeadline value is 20.0
    case subHeadline = 20.0
    /// Body value is 16.0
    case body = 16.0
    /// Caption value is 14.0
    case caption = 14.0
    /// SmallCaption value is 12.0
    case smallCaption = 12.0
}
